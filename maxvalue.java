package ExCa;
public class maxvalue {

    public static void main(String args[]) {
        System.out.println("Range of Byte="+Byte.MIN_VALUE+"-"+Byte.MAX_VALUE);
        System.out.println("Range of Short="+Short.MIN_VALUE+"-"+Short.MAX_VALUE);
        System.out.println("Range of Integer="+Integer.MIN_VALUE+"-"+Integer.MAX_VALUE);
        System.out.println("Range of Long="+Long.MIN_VALUE+"-"+Long.MAX_VALUE);
        System.out.println("Range of Float="+Float.MIN_VALUE+"-"+Float.MAX_VALUE);
        System.out.println("Range of Double="+Double.MIN_VALUE+"-"+Double.MAX_VALUE);
        
        System.out.println("Character="+Character.MAX_VALUE);
        System.out.println((int)Character.MAX_VALUE);
        
        float c=2.55f;
        int d=4;
        System.out.println(d+c);
        
        System.out.println("Perimeter of rectangle="+((float)(2*(10.0f+15.5f))));
    }
}
